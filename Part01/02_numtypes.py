age = input("Hello hacker, what is your age? ")
age_plus5 = int(age) + 5
print("In five years you will be {}".format(age_plus5))
age_times10 = int(age) * 10
print("when you live ten times as long you will be {}".format(age_times10))
age_quotient = int(int(age) / 3)
age_remainder = int(age) % 3
print("Your age divided by 3 equals {} remainder {}".format(age_quotient, age_remainder))